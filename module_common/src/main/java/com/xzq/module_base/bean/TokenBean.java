package com.xzq.module_base.bean;

public class TokenBean {
    private int code;
    private String msg;
    private String result;
    public TokenBean(){}
    public TokenBean(int code, String msg, String result) {
        this.code = code;
        this.msg = msg;
        this.result = result;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }
}
